package Pertemuan2;

public class LatihanVariabel {
    public static void main(String[] args){
        int myNum = 15;
        float myFloatNumber = 165.79f;
        char myChar = 'L';
        boolean myBool = true;
        String myText = "Hafidh Muhammad Akbar";

        System.out.println("Nama = " +myText);
        System.out.println("Jenis kelamin = " +myChar);
        System.out.println("Tinggi badan = " +myFloatNumber);
        System.out.println("Tanggal lahir = " +myNum);
        System.out.println("Status = " +myBool);
    }
}
